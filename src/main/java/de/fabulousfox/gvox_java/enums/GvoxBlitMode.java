package de.fabulousfox.gvox_java.enums;

/**
 * Defines the blit mode of the GvoxContext.
 */
public enum GvoxBlitMode {
    /**
     * The blit mode is not set (LEGACY SUPPORT).
     */
    DONT_CARE,
    /**
     * The blit mode is set to PARSE_DRIVEN.
     */
    PARSE_DRIVEN,
    /**
     * The blit mode is set to SERIALIZE_DRIVEN.
     */
    SERIALIZE_DRIVEN
}
